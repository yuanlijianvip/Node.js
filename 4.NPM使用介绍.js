// npm list -g

// NPM常用命令
// 1.npm help
// 2.npm help <command>
// 3.在package.js所在目录下使用npm install . -g可先在本地安装当前命令行程序，可用于发布前的本地测试。
// 4.npm uodate <package>
// 5.npm uodate <package> -g
// 6.npm cache clear可以清空NPM本地缓存
// 7.npm unpublish <package>@<version>可以撤销发布自己发布过的某个版本代码


